# DOC_ID prefix 인식용 — 한글 약칭 + 정식 명칭 모두
DOC_ID_PREFIXES: frozenset[str] = frozenset(
    list(KOR_ABBREV_TO_FULL.keys()) + list(KOR_ABBREV_TO_FULL.values())
)


def normalize_agency(token: str) -> str | None:
    """약칭(한글/영문)을 정식 명칭으로 정규화."""
    if token in KOR_ABBREV_TO_FULL:
        return KOR_ABBREV_TO_FULL[token]
    if token in ENG_ABBREV_TO_KOR:
        return ENG_ABBREV_TO_KOR[token]
    return None


def is_doc_id_prefix(token: str) -> bool:
    return token in DOC_ID_PREFIXES
